export const PART_ID_PREFIX = 'part-';
export const STORAGE_KEY = 'carPartsChecklistState.v2';

export function createPartId(
  categoryName: string,
  part: { name: string }
): string {
  return (
    PART_ID_PREFIX +
    categoryName.replace(/\s+/g, '_') +
    '-' +
    part.name.replace(/[^\w]+/g, '_')
  ).toLowerCase();
}
